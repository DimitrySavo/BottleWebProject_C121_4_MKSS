class DegreesTests(unittest.TestCase):
    def setUp(self):
        self.graphFifteenNodes = Graph(15)

    def test_degrees_no_edges(self):
        # Тестирование функции degrees() при отсутствии ребер
        self.graphFifteenNodes.matrix = [[0] * 15 for _ in range(15)]
        self.assertEqual(self.graphFifteenNodes.degrees(), [0] * 15)

